// 2. Write a program in which, declare all data types like integer, double, float, long integer, character and byte data and print them.
public class DataType {
    public static void main(String[] args) {
        String name = "sanjai"; 
        char initial = 'M';
        byte age = 21;
        short departMentCode = 1001;
        int regNo = 257411;
        long phoneNo = 6374388946l;
        float height = 180.7f;
        double weight = 55.74;
        boolean isPass = true;
        System.out.println("Name : "+name);
        System.out.println("Initial : "+initial);
        System.out.println("Age : "+age);
        System.out.println("DepartMent Code : "+departMentCode);
        System.out.println("Register No : "+regNo);
        System.out.println("Phone No : "+phoneNo);
        System.out.println("Height : "+height);
        System.out.println("Weight : "+weight);
        System.out.println("Pass : "+isPass);
    }
}
